#!/bin/bash

cd "$(dirname "${BASH_SOURCE[0]}")" \
    && . "utils.sh"

# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

create_directories() {

    declare -a DIRECTORIES=(
        "$HOME/bin"
        "$HOME/git"
        "$HOME/go"
        "$HOME/logs"
        "$HOME/notes"
        "$HOME/private"
        "$HOME/work"
        "$HOME/.config/i3"
        "$HOME/Pictures/screenshots"
    )

    for i in "${DIRECTORIES[@]}"; do
        mkd "$i"
    done

}

# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

main() {
    print_in_purple "\n â€¢ Create directories\n\n"
    create_directories
}

main
